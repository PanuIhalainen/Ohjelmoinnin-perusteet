# Tee Python-ohjelma ja lisää siihen seuraavat proseduurit:

# 1. 47Anna muuttujalle arvoNum1
Num1 = 47
# 2. Anna 102muuttujalle arvoNum2
Num2 = 102
# 3. Laske muuttujat Num1ja Num2yhteen ja kirjoita tulos Summuuttujaan
Sum = Num1 + Num2
# 4. Vähennä : Num1sta Num2ja tallenna sitten tulos muuttujaan Diff.
Diff = Num2 - Num1
# 5. Kerro Sumja Diff, ja sijoita sitten tuloksena oleva tulo Product.
Product = Sum * Diff
# 6. Tulosta summaoperaatio “ {Num1} + {Num2} = {Sum}”
print(Num1, "+", Num2, "=", Sum )
# 7. Tulosta alioperaatio “ {Num2} - {Num1} = {Diff}”
print(Num2, "-", Num1, "=", Diff)
# 8. Tulosta kertolasku “ {Sum} * {Diff} = {Product}”
print(Sum,"*", Diff, "=", Product)
# 9. Tulosta yhteen-, osalasku- ja kertolaskutoimitukset yhdessä. Katso kohta ”ohjelman suoritus” alla.
print("(", Num1,"+", Num2,") * (", Num2, "-", Num1, ") =", Product)
